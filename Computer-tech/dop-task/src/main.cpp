 const char *FNAME = "tests/test11.txt";      // Цель теста 11: проверить работоспособность программы при корректном вводе (макс элемент в конце)
 // const char *FNAME = "tests/test12.txt";      // Цель теста 12: проверить работоспособность программы при корректном вводе
 
 
 const char *FNAME_RES = "result.txt";  // имя выходного файла для результатов
 const int MAX_SIZE = 100;              // максимальныйразмер массива
 
 /*******************************************************************/
 /*               О С Н О В Н А Я     П Р О Г Р А М М А             */
 /*******************************************************************/
 
int main()
{
  // Объявление переменных
  int n;                       // размер массива
  ifstream fin;
  ofstream fout;
  int i;                       // счетчик циклов
  float* A;                    // указатель на массив
  int maxIndex;                // индекс максимального по модулю элемента
  float maxAbs;                // максимальное значение по модулю
  float sum;                   // сумма элементов до максимального
  
  // Открытие входного файла
  fin.open(FNAME);
  fin;
  if (fin.fail()) {
    cout << "Ошибка: не удалось открыть файл " << FNAME << endl;
    return 1;
  }
  
  // Проверка на пустой файл
  if (fin.peek() == EOF || fin.eof()) {
    cout << "Ошибка: файл пустой" << endl;
    fin.close();
    return 1;
  }
  
  // Чтение размера массива
  fin >> n;
  if (fin.fail()) {
    cout << "Ошибка: некорректные данные о размере массива" << endl;
    fin.close();
    return 1;
  }
  
  // Проверка размера массива
  if (n < 1 || n > MAX_SIZE) {
    cout << "Ошибка: размер массива должен быть от 1 до " << MAX_SIZE << endl;
    fin.close();
    return 1;
  }
  
  // Выделение памяти и чтение элементов массива
  A = new float[n];
  for (i = 0; i < n; i++) {
    fin >> A[i];
    if (fin.fail()) {
      fin.clear(); // очищаем флаги ошибки, чтобы проверить причину
      if (fin.eof() || fin.peek() == EOF) {
        cout << "Ошибка: недостаточно элементов массива в файле" << endl;
      } else {
        cout << "Ошибка: некорректные элементы массива" << endl;
      }
      delete[] A;
      fin.close();
      return 1;
    }
  }
  fin.close();

  // Вывод исходного массива
  cout << "Исходный массив: ";
  for (i = 0; i < n; i++) {
    cout << A[i] << " ";
  }
  cout << endl;

  // Поиск индекса максимального по модулю элемента
  maxIndex = 0;
  maxAbs = fabs(A[0]);
  for (i = 1; i < n; i++) {
    if (fabs(A[i]) > maxAbs) {
      maxAbs = fabs(A[i]);
      maxIndex = i;
    }
  }
  
  cout << "Максимальный по модулю элемент: " << A[maxIndex] 
       << " (индекс " << maxIndex << ")" << endl;

  // Вычисление суммы элементов до максимального по модулю
  sum = 0.0;
  for (i = 0; i < maxIndex; i++) {
    sum += A[i];
  }
  
  cout << "Сумма элементов до максимального по модулю: " << sum << endl;

  // Вывод элементов после максимального по модулю в обратном порядке
  cout << "Элементы после максимального по модулю (в обратном порядке): ";
  for (i = n - 1; i > maxIndex; i--) {
    cout << A[i] << " ";
  }
  cout << endl;

  // Запись результатов в файл
  fout.open(FNAME_RES);
  
  fout << "Сумма элементов до максимального по модулю: " << sum << endl;
  fout << "Элементы после максимального по модулю (в обратном порядке): ";
  for (i = n - 1; i > maxIndex; i--) {
    fout << A[i] << " ";
  }
  fout << endl;
  fout.close();
  
  cout << "Результаты записаны в файл " << FNAME_RES << endl;

  // Освобождение памяти
  delete[] A;

  return 0;  // успешное завершение программы
}
 
